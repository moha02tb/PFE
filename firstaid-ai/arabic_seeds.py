def expand_arabic_seeds() -> list[dict]:
    """Return one row per (scenario, question phrasing) - 20 x 10 = 200 rows."""
    rows: list[dict] = []
    for scenario in ARABIC_FIRSTAID:
        answer = scenario["answer"]
        category = scenario["category"]
        severity = scenario["severity"]
        name = scenario["name"]
        for q in scenario["questions"]:
            rows.append(
                {
                    "question": q,
                    "answer": answer,
                    "source": f"curated/arabic/{name}",
                    "category": category,
                    "severity_hint": severity,
                }
            )
    return rows


